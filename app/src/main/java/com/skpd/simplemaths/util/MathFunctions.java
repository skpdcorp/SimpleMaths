package com.skpd.simplemaths.util;

public class MathFunctions {

    public long add(long first, long second){
        return first+second;
    }

    public long subtract(long first, long second) {
        return  first-second;
    }

    public long multiple(long first, long second) {
        return first*second;
    }

    public long divide (int first, int second) {
        return first/second;
    }
}
